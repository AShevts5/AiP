#include <iostream>    
#include <random>      
#include <vector>      
#include <string>      
#include <sstream>    

using namespace std;

int main() {
    
    random_device rd;
    mt19937 gen(rd());
    uniform_int_distribution<> dis(1, 10);

    //ГЕНЕРАЦИЯ СТРОКИ ИЗ СЛУЧАЙНЫХ СЛОВ
    // Создаем пустую строку для хранения сгенерированной строки
    string randomString;
    
    // Цикл для генерации 10 слов
    for (int i = 0; i < 10; ++i) {
        // Генерируем случайную длину слова от 1 до 10
        int length = dis(gen);
        
        // Создаем пустую строку для текущего слова
        string word;
        
        // Вложенный цикл для генерации каждого символа слова
        for (int j = 0; j < length; ++j) {
            // Генерируем случайную букву от 'a' до 'z'
            char c = 'a' + dis(gen) % 26;
            // Добавляем символ к текущему слову
            word += c;
        }
        
        // Добавляем сгенерированное слово к общей строке
        // После каждого слова добавляем пробел
        randomString += word + " ";
    }
    // После цикла randomString содержит 10 слов, разделенных пробелами

    //РАЗБИЕНИЕ СТРОКИ НА СЛОВА
    
    // istringstream - строковый поток ввода
    istringstream iss(randomString);
    // Вектор для хранения слов
    vector<string> words;
    
    // Временная переменная для хранения каждого слова
    string word;
    
    // Цикл while читает слова из строкового потока
    // Оператор >> из потока iss читает одно слово (до пробела) в переменную word
    while (iss >> word) {
        words.push_back(word);
    }
    // После цикла вектор words содержит все слова из строки randomString

    //СКЛЕИВАНИЕ СЛОВ
    
    // Создаем строку для хранения результата
    string result;
    
    // Получаем количество слов
    int n = words.size();
    
    // Цикл для склеивания пар слов
    // i идет от 0 до n/2 - 1
    for (int i = 0; i < n / 2; ++i) {
        // Склеиваем i-е слово с конца и слово с конца (n-1-i)
        // Добавляем пробел после каждой пары
        result += words[i] + words[n - 1 - i] + " ";
    }
    
    // Если количество слов нечетное (n % 2 == 1)
    if (n % 2 == 1) {
        // Добавляем среднее слово без пары
        // Индекс среднего слова: n / 2 (при целочисленном делении)
        result += words[n / 2] + " ";
    }
    
    // ВЫВОД РЕЗУЛЬТАТОВ
    
    // Выводим исходную сгенерированную строку
    cout << "Сгенерированная строка: " << randomString << endl;
    
    // Выводим результат склеивания
    cout << "Результат склеивания: " << result << endl;
    
    return 0;
}
