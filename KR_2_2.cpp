#include <iostream>
#include <iomanip>     // Для форматирования вывода
#include <vector>      // Для работы с динамическими массивами

using namespace std;   

int main() {
    //УСТАНОВКА РАЗМЕРА МАТРИЦЫ
    int n = 10;  // По условию задачи n = 10
    
    //СОЗДАНИЕ КВАДРАТНОЙ МАТРИЦЫ n x n
    vector<vector<int>> matrix(n, vector<int>(n));
    
    //ЗАПОЛНЕНИЕ МАТРИЦЫ ПО ЗАДАННОМУ ОБРАЗЦУ
    for (int i = 0; i < n; i++) {
        // Проверяем, четная или нечетная строка
        if (i % 2 == 0) {  
            for (int j = 0; j < n; j++) {
                matrix[i][j] = j + 1;  
            }
        } else {  
            for (int j = 0; j < n; j++) {
                matrix[i][j] = n - j;  
            }
        }
    }
    
    //ВЫВОД МАТРИЦЫ НА ЭКРАН
    cout << "Квадратная матрица " << n << "x" << n << ":" << endl;
    cout << "==================================================" << endl;
    
    for (int i = 0; i < n; i++) {
        // Выводим номер строки для наглядности
        cout << "Строка " << setw(2) << (i + 1) << ": ";
        
        // Выводим элементы строки
        for (int j = 0; j < n; j++) {
            cout << setw(3) << matrix[i][j] << " ";
        }
        cout << endl;
    }
    
    cout << "==================================================" << endl;
    
    return 0;
}
